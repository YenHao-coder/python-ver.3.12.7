print('a=40 , b=5') #a=40 , b=5
operator=input('請輸入要執行的運算 (+、-、*、/): ') #請輸入要執行的運算 (+、-、*、/):
if operator=='+':
    print('a + b = 45') 
elif operator=='-':
    print('a - b = 35')
elif operator=='*':
    print('a * b = 200')
elif operator=='/':
    print('a / b = 8.0')
else:
    print('無法執行運算!') #列印運算過程與結果